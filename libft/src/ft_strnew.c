#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*str;

	str = NULL;
	if ((str = (char *)malloc(sizeof(char *) * size + 1)))
	{
		while (size)
			str[size--] = '\0';
		str[0] = '\0';
	}
	return (str);
}
