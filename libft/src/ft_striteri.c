#include "libft.h"

void	ft_striteri(char *s, void (*f) (unsigned int, char *))
{
	int i;
	int l;

	i = 0;
	if (s != NULL && f != NULL)
	{
		l = ft_strlen(s);
		while (i < l)
		{
			f(i, &s[i]);
			i++;
		}
	}
}
