#include "includes/libft.h"

void	*ft_memalloc(size_t size)
{
	char	*alloc;
	size_t	i;

	i = 0;
	if (!(alloc = (char *)malloc(sizeof(char) * size)))
		return (NULL);
	while (i < size)
	{
		alloc[i] = '\0';
		i++;
	}
	return (alloc);
}
